public class MetodosTexto {
    
    
    public void metodoTexto1(String texto) {
        System.out.println("Texto recibido: " + texto);
    }

    
    public void metodoTexto2(String palabra1, String palabra2) {
        System.out.println("Concatenación: " + palabra1 + " " + palabra2);
    }

    
    public void metodoTexto3(String nombre) {
        System.out.println("¡Hola, " + nombre + "!");
    }

    
    public void metodoTexto4(String frase) {
        System.out.println("Frase ingresada: " + frase);
    }

    
    public void metodoTexto5(String texto) {
        System.out.println("Longitud del texto: " + texto.length());
    }
}
